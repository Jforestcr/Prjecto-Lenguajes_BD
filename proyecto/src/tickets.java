    public void consultar_ticket(Connection a) throws IOException {
        int id = -1;

        try {
                       
            // Llamada al procedimiento almacenado
            CallableStatement cst = a.prepareCall("{call mostrar_tiquete (?,?,?,?,?,?)}");

            do {
                
                // Parametro 1 del procedimiento almacenado
                cst.setInt(1, Integer.parseInt(combox_ID_tiquete.getSelectedItem().toString()));
                
                // Definimos los tipos de los parametros de salida del procedimiento almacenado
                cst.registerOutParameter(2, java.sql.Types.VARCHAR);
                cst.registerOutParameter(3, java.sql.Types.VARCHAR);
                cst.registerOutParameter(4, java.sql.Types.VARCHAR);
                cst.registerOutParameter(5, java.sql.Types.VARCHAR);
                cst.registerOutParameter(6, java.sql.Types.VARCHAR);
              

                
                // Ejecuta el procedimiento almacenado
                cst.execute();
                
                // Se obtienen la salida del procedimineto almacenado
                String usuario = cst.getString(2);
                String categoria = cst.getString(3);
                String titulo = cst.getString(4);
                String descripcion = cst.getString(5);
                String fechacrea = cst.getString(6);
                

                text1.setText(usuario);
                text2.setText(categoria);
                text3.setText(titulo);
                texta1.setText(descripcion);
                text5.setText(fechacrea);
                
            } while (id > 0);

        } catch (SQLException ex) {
            System.out.println("Error: " + ex.getMessage());
        } finally {
            try {
                a.close();
            } catch (SQLException ex) {
                System.out.println("Error: " + ex.getMessage());
            }

    }
            }
    
        public void Obterner_id_ticket(Connection a) {
        try {
            Statement sentencia;
            String sql = "SELECT tick_id FROM tm_ticket";
            sentencia = a.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet RS = sentencia.executeQuery(sql);
            while (RS.next()) {
                String identificacion = RS.getString("tick_id");
                combox_ID_tiquete.addItem(String.valueOf(identificacion));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }

